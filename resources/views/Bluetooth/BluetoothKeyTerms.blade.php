    <span class="darkBlueText">Bluetooth: </span> A wireless computing and telecommunications specification for personal area networks (PANs). Used to define a communication protocol for the interaction of mobile personal computing devices and computers. Operates in a close range around 10 metres, data rate up to 1Mbps at 2.4GHz.
    <a href="#top">

    </a><br>
    <br>

    <span class="darkBlueText">Piconet: </span>The smallest unit of Bluetooth communication. It is comprised of one Master and up to seven Slaves. Devices synchronize their frequency hopping pattern and internal clock.The Master dictates the frequency hopping pattern and clock.
    <a href="#top">[Back to Top]</a><br>
    <br>

    <span class="darkBlueText">Wireless Transmission: </span>Data transmission using unguided media. <a href="#top">[Back to Top]</a><br>
    <br>

    <span class="darkBlueText">Wireless: </span>A network connection which deploys the transmission of electromagnetic frequency through the atmosphere to carry data transmissions. <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Wavelength: </span>The propagation speed of a signal divided by its frequency.  <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Bluetooth wireless technology: </span> Bluetooth wireless technology is a wireless communication link, operating in the unlicensed ISM band at 2.4 GHz using a frequency hopping transceiver. It allows real-time AV and data communications between Bluetooth enabled hosts. The link protocol is based on time slots. <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Transmitter:</span>A communications device used to broadcast radio signals. <a href="#top">

    </a><br>
    <br>
    <span class="darkBlueText">Bluetooth Baseband: </span> The part of the Bluetooth system that specifies or implements the medium access and physical layer procedures to support the exchange of real-time voice, data information streams, and ad hoc networking between Bluetooth enabled devices.
    <a href="#top"></a><br>
    <br>
    <span class="darkBlueText">Bluetooth Clock: </span>A 28 bit clock internal to a Bluetooth controller sub-system that ticks every 312.5 ms. The value of this clock defines the slot numbering and timing in the various physical channels. <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Bluetooth Cobtroller:</span> A sub-system containing the Bluetooth RF, baseband, resource controller, link manager, device manager and a Bluetooth HCI.
    <a href="#top"></a><br>
    <br>
    <span class="darkBlueText">Bluetooth Enabled Device:</span>A Bluetooth enabled device is a device that is capable of short-range wireless communications using the Bluetooth system.
    <a href="#top"></a><br>
    <br>
    <span class="darkBlueText">Baseband Controller (Link Controller):</span>The Baseband Controller performs the following tasks: Manages physical channels and links for single and multiple link; selects hopping frequency; forms piconets and scatternets; formats packets and sends them for transmission; Inquiry and Inquiry Scan; Connection and Page Scan; Encrypts Data; Manages Power
    . <a href="#top"></a><br>
    <br>

    <span class="darkBlueText">Piconet:</span> The smallest unit of Bluetooth communication. It is comprised of one Master and up to seven Slaves. Devices synchronize their frequency hopping pattern and internal clock.The Master dictates the frequency hopping pattern and clock. <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Bluetooth Device Address:</span> A 48 bit address used to identify each Bluetooth enabled device. Often this is referred to in technical specifications as BD_ADDR. <a href="#top">[Back to Top]</a><br>
    <br>

    <span class="darkBlueText">Basic Piconet Channel:</span> A channel for communication between devices in a piconet.
    Adapted Piconet Channel: For devices in a piconet which are AFH (Adaptive Frequency Hopping) enabled. <a href="#top">[Back to Top]</a><br>
    <br>

    <span class="darkBlueText">Adapted Piconet Channel:</span>  For devices in a piconet which are AFH (Adaptive Frequency Hopping) enabled.
    . <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">BD_ADDR:</span> The Bluetooth device address, BD_ADDR, is used to identify a Bluetooth enabled device.
    . <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Bluetooth HCI:</span> The Bluetooth HCI provides a command interface to the baseband controller and link manager and access to hardware status and control registers. This interface provides a uniform method of accessing the Bluetooth baseband capabilities. <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Bluetooth Host: </span>A Bluetooth Host is a computing device, peripheral, cellular telephone, access point to PSTN network or LAN, etc. A Bluetooth Host attached to a Bluetooth Controller may communicate with other Bluetooth Hosts attached to their Bluetooth Controllers as well.

    <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Biuetooth Profiles: </span>Bluetooth profiles are general behaviors through which Bluetooth enabled devices communicate with other devices. Bluetooth technology defines a wide range of profiles that describe many different types of use cases. In order to use Bluetooth technology, a device must be able to interpret certain Bluetooth profiles. The profiles define the possible applications.
    <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Synchronous Connection Oriented (SCO) Logical Transport: </span>SCO provides logical transport support for continuous data transfer in a circuit-switch connection. Up to three SCO links may be upheld between one Master and Slave. Up to two SCO links may be upheld between a Slave and Two Masters. A Master can uphold an SCO and ACL transport at the same time with a slave.
    <a href="#top"> [Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Extended Synchronous Connection Oriented (eSCO) Logical Transport: </span> eSCO is a point-to-point link between a Master and Slave which may be symmetric or asymmetric, meaning that it is possible to use a 3-slot packet in one direction and a 1-slot packet in the other direction. eSCO is a circuit-switch connection which allows for retransmissions of corrupted packets and higher data rates compared to SCO links. eSCO is commonly used to route a call through a Bluetooth headset.
    . <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Packets:</span> Packets refer to the information sent between Bluetooth devices. All packet transmissions must initiate with an access code used for synchronization. Packets have two formats: Standard Basic Rate (BR) and Standard Enhanced Data Rate (EDR).
    <a href="#top">[Back to Top]</a><br>
    <br>
    <span class="darkBlueText">Standard Basic Rate (BR): </span> A packet prefixed by an access code, header, and followed by the data payload.
    <a href="#top"></a><br />
    <br />
    <span class="darkBlueText">Standard Enhanced Data Rate (EDR):</span> A packet prefixed by an access code, header, guard, sync, payload, and book-ended with a trailer.
    . <a href="#top"></a><br />
    <br />
    <span class="darkBlueText">Asynchronous Connection Oriented (ACL) Logical Transport:</span> ACL is used to carry broadcast traffic, user data, and control signals between a Master and Slaves in a packet-switch connection. ACL has high bandwidth and is used for music transfer.
    <a href="#top"> </a><br />
    <br />
    <span class="darkBlueText">POLL: </span>Used by the Master to ask Slaves for their status. Contains no payload. If Slaves have no data to transmit, they respond to the Master with a NULL packet.
    FHS: Frequency Hop Synchronizations (FHS) is used to synchronize hop frequencies during piconet establishment.
    <a href="#top"></a><br />
    <br />
    <span class="darkBlueText">FHSS: </span>Stands for Frequency Hopping Spread Spectrum. A technology normally used in wireless LANs. FHSS operates by transmitting short bursts of data on different frequencies. One burst is transmitted on one frequency, a second burst is transmitted on a second and different frequency, and so forth. <a href="#top">[Back to Top]</a><br />
    <br />
    <span class="darkBlueText">Basic Piconet Channel:</span>A channel for communication between devices in a piconet. <a href="#top">[Back to Top]</a><br />
    <br />
    <span class="darkBlueText">Baseband Controller (Link Controller): </span>The Baseband Controller performs the following tasks: Manages physical channels and links for single and multiple link; selects hopping frequency; forms piconets and scatternets; formats packets and sends them for transmission; Inquiry and Inquiry Scan; Connection and Page Scan; Encrypts Data; Manages Power
    . <a href="#top">[Back to Top]</a><br />
    <br />
    <span class="darkBlueText">Adaptive Frequency Hopping: </span> A tactic to reduce interference by other devices in an ISM band. When AFH is enabled, the Master marks interference ridden channels as unused which removes these channels from the frequency hopping pattern. AFH can reduce the number of channels in use to 20.
