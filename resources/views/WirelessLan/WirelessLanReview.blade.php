		  <p><span class="darkBlueText">Question 9: </span>In mobile telephony, what is the name of the unit serviced by one transmitter? </p>
          <p><span class="redText">Answer:</span> A cell.</p>
           <br />
		  <p><span class="darkBlueText">Question 10:</span> Explain why terrestrial microwave transmission can cover only a limited distance range. </p>
          <p><span class="redText">Answer:</span> The shape of the terrain and the height of the antenna determine how far terrestrial microwaves can travel.</p>
           <br />
		  <p><span class="darkBlueText">Question 11:</span> Two Earth stations are connected via a satellite. Each site's LAN includes a bridge, which in turn is connected to the site's antenna. The antennae transmit to the satellite and receive a signal from the satellite. Why does the satellite link (i.e., connection) described above require two bridges? </p>
          <p><span class="redText">Answer:</span> To avoid longer delay in communicating two sites. For example, if one bridge is used instead of two, then all the frames (packets) from one site have to be transmitted to the other site via satellite before the bridge can filter the frames.</p>
           <br />
		  <p><span class="darkBlueText">Question 12:</span> What are the main advantages of a wireless LAN? </p>
          <p><span class="redText">Answer:</span> User mobility and flexibility with an added advantage of &quot;no cable&quot;. Both cable and the installation cost can be saved.</p>
           <br />
		  <p><span class="darkBlueText">Question 13:</span> What are the main disadvantages of a wireless LAN? </p>
          <p><span class="redText">Answer: </span>Lower bandwidth and data rate compared to Wired LAN. Signal interference from various sources such as walls, ceiling, devices operating with the same frequency. The quality of signal may be affected by the weather condition as well.</p>
           <br />
		  <p><span class="darkBlueText">Question 14:</span> What does the term modulation refer to? </p>
          <p><span class="redText">Answer:</span> The term modulation is used to refer to the process of changing the characteristics of the frequency of the carrier signal.</p>
