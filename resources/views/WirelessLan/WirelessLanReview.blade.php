		  <p><span class="darkBlueText">Question 4: </span>Identify two types of radio waves which use line-of-sight propagation, and the associated operational ranges. </p>
          <p><span class="redText">Answer:</span> Very High Frequency (VHF) from 30 MHz to 300 MHz, and Ultra High Frequency (UHF) from 300 MHz to 3 GHz.</p>
