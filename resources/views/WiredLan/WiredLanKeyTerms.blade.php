            <span class="darkBlueText" id="1">10BaseT:</span> The technical
            name for unshielded twisted pair wiring running at 10 Mbps, used for
            Ethernet LANs. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">10Base5:</span> IEEE 802.3
            Ethernet LAN specification using thick coaxial cable operating at 10
            Mbps with a distance limit of 500 meters per segment. This is an
            older technology for Ethernet LANs. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">10Base2:</span> IEEE 802.3
            Ethernet LAN specification using thin coaxial cable operating at 10
            Mbps with a distance limit of 185 meters per segment. This is an
            older technology for Ethernet LANs. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="a">Access method: </span>This term
            generally refers to a scheme by which stations may access a network
            via a shared medium. See also <i>channel access protocol</i>. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">ACK: </span>Generally refers to
            the ACKnowledgment of the receipt of the last transmission.<a href="#top"> [Back to Top]</a><br><br>

            <span class="darkBlueText">Attenuation: </span>The
            continuous loss of the signal's strength as it travels through a
            medium.<a href="#top"> [Back to Top]</a><br><br>

            <span class="darkBlueText" id="b">Backbone: </span>A network of
            high-speed communication lines that carries the bulk of the traffic
            between major segments of the networks. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Backoff: </span>The
            retransmission delay (usually random) enforced by contention media
            access control protocols after a station that wanted to transmit
            sensed a carrier on the physical medium. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Bandwidth:</span> In general, the
            theoretical capacity (in bits per second or slots per second) of a
            data communication channel. For example, the bandwidth of a Fast
            Ethernet network is 100 Mbps. The greater the bandwidth, the higher
            the possible data transmission rate. However, the technical
            definition of bandwidth is as follows: "the bandwidth of a channel
            is the difference between the highest and the lowest frequency of a
            communication channel that can be transmitted over a medium". For
            example, the public telephone network has a voice channel bandwidth
            of 3.1 KHz. Each channel can pass frequencies in the range of 300 Hz
            to 3,400 Hz. Therefore, the bandwidth is 3,100 Hz, or 3.1 KHz. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Baseband: </span>This term
            refers to the characteristic of a networking technology in which the
            entire channel capacity of the medium is used by one data signal.
            Thus, only one node transmits at a time. Ethernet LAN is an example
            of a baseband network. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Baud:</span> The number of
            changes in a signal per second. Each change can encode one or more
            bits of information. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Bit:</span> Short for "Binary
            digit". This is the unit used in the binary numbering system. A bit
            can be either 0 or 1. Normally, a sequence of 8 bits operates as one
            "octet" (byte). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Bit Rate:</span> The speed at
            which bits are transmitted, usually expressed in bits per second
            (bps)
            . See also <i>bps</i>.<a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">BNC:</span> Bayonet Naval
            Connector or Bayonet Nut Connector or British Naval Connector. The
            type of connector used with thin coaxial cable for Ethernet LAN having
            a bayonet-like shell.<a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">bps:</span> Stands for bits per
            second and represents the rate at which data can be transmitted
            across a network. The number of bits per second may differ from the
            baud rate because more than one bit can be encoded in a single baud. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Broadband: </span>A broadband
            transmission employs several transmission channels on a single
            physical medium. Thus, more than one node can transmit at a time. In
            New Zealand., Telecom's "Jetstream" is an is an example of a
            broadband technology. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Bus Topology:</span> A LAN
            architecture in which all computers are attached to a shared medium
            - often a single coaxial cable terminated at each end. One computer
            transmits at a time; the signal travels along the bus (both ways) and
            is received by all other stations. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Byte: </span>A group of eight
            bits that are operated upon as a unit. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="c">Carrier:</span> This term refers
            to the basic signal transmitted across a network. A carrier is a
            modulated (i.e. changed) to encode data. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Cat 5e:</span> This is an
            extension to UTP Cat 5 cable which supports rates above 100 Mbps. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Channel</span>: A single
            communication path (e.g. cable media or wireless) over which a
            message flows. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Channel Access Protocol:</span>
            Also known as media access control (MAC) protocol. A protocol (i.e.,
            set of rules) which is used to access the shared channel on the
            network. Examples of access protocols are Ethernet CSMA/CD, and Token
            passing. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Coaxial Cable:</span> A type of
            cable used for computer networks as well as for cable television. A
            coaxial cable consists of a central core that carries the signal. It
            has a layer of insulation, a layer of shielding, and an outer
            insulation jacket. The shield protects the signal on the inner wire
            from electrical interference. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Collision:</span> An event that
            occurs on a CSMA/CD network when two stations attempt to transmit
            simultaneously. The signals interfere with each other, forcing the
            two stations to back off and try again. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Collision Detection:</span> A
            network management technique that allows each computer to transmit
            whenever it chooses. If a collision is detected, the messages are
            retransmitted. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">CRC:</span> Stands for Cyclic
            Redundancy Check. An error detection technique that typically adds
            either 16 or 32 "check" bits to potentially large data packets
            and provides almost 100 percent error detection. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">CSMA:</span> Stands for Carrier
            Sense Multiple Access. This is a channel access method in which a
            station "senses" the channel (i.e. "listens" to the channel)
            before sending a packet into the network - trying to find out
            whether or not another station is attempting to send a signal at the
            same time. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">CSMA/CD:</span> Stands for
            Carrier Sense Multiple Access with Collision Detection. This is a
            popular access method used by Ethernet LAN based on carrier sensing.
            Under this scheme, a computer senses the channel and transmits data
            if the channel is found to be idle (i.e. free). If the channel is
            busy, the station continues to sense (i.e. listen) the channel until
            the channel is idle, then transmits immediately. If a collision is
            detected during transmission, the station ceases transmission
            immediately. After a collision, a station waits a randomly determined
            amount of time, then attempts to transmit again. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="d">Data Rate: </span>Data rate is
            defined as the speed (in bits per second) at which data is
            transmitted through the channel. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">DB-25: </span>A 25-pin
            standardised connector used in personal computers for parallel output
            ports (female connector on IBM PC chassis) or for serial I/O ports
            (male connector on IBM PC chassis). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">DNS: </span>Stands for Domain Name System. An application layer protocol in the
            TCP/IP
            protocol stack which resolves host computer domain names to IP addresses and vice-versa. <a href="#top">[Back
                to
                Top]</a><br><br>
            <span class="darkBlueText">Domain Name System: </span><em>See DNS.</em> <a href="#top">[Back
                to Top]</a><br>
            <br>

            <span class="darkBlueText">DQDB: </span>Stands for
            Distributed Queue Dual Bus. A high-speed backbone network as proposed
            by the IEEE 802.6 committee for use in metropolitan area networks. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Driver: </span>Software that
            enables a computer to communicate with devices like NICs, printers,
            monitors, and hard drives. Each driver has a specific purpose, such
            as handling Ethernet network communications. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="e">Ethernet</span>: A popular LAN
            technology (invented by Xerox Corporation and developed jointly by
            Xerox, Intel, and Digital Equipment Corporation) that uses a shared
            channel and the CSMA/CD access method. Basic Ethernet operates at 10
            Mbps, Fast Ethernet operates at 100 Mbps, and Gigabit Ethernet
            operates at 1000 Mbps (i.e., 1 Gbps). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="f">Fast Ethernet: </span>A class
            of Ethernet technology that operates at the rate of 100 Mbps. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">FDDI: </span>Stands for Fibre
            Distributed Data Interface. This is a 100 Mbps token passing
            network using dual-ring fiber-optic cable. This type of network is
            used as backbone network with a coverage of up to 40 km; can serve as
            a metropolitan area network, linking various LANs to WANs. See also
            <i>MAN</i>. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">File server:</span> A file server
            is a network computer that makes software applications, data files,
            and network utilities available to other network computers. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Frame: </span>The formatted
            packet that the underlying hardware accepts and delivers. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Full Duplex:</span> A mode of
            data transmission in which data is transmitted in both directions
            (from sender to receiver and from receiver to sender) at the same
            time. Multiple channels are required for this type of communication. <a href="#top">[Back to
                Top]</a><br><br>

            <span class="darkBlueText" id="g">Gbps: </span>Stands for Giga bits
            per second. A unit of data transfer equal to 1000 Mbps or 10<font size="2"><sup>9</sup></font>
            bps. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Gigabit Ethernet</span>: A
            version of Ethernet technology that operates at the rate of 1000 Mbps
            (i.e. 1 Gbps). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">GUI</span>: Stands for Graphical
            User Interface. Most of the modern operating systems provide a GUI,
            which enables a user to use a pointing device, such as a computer
            mouse, to provide the computer with information about the user's
            intentions. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Guided Media: </span>Also called
            cable media. Fibre optics, coaxial cable and twisted-pair
            (telephone lines) are all called guided media because the recipient
            of the data is physically connected to the transmission source. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="h">Half Duplex:</span> A mode of
            data transmission in which data can be sent in both directions but
            only one direction at a time. The walkie-talkie is an example of
            half-duplex communication. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Hertz: </span>The unit to measure
            frequency or bandwidth (abbreviated to Hz). It represents the number of
            cycles or oscillations per second. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Hub:</span> A device (a multiport
            connector) that interconnects two or more workstations in a
            star-wired local area network and broadcasts incoming data onto all
            outgoing connections. To avoid signal collision only one user can
            transmit data through the hub at a time. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="i">IEEE: </span>IEEE Stands for
            Institute of Electrical and Electronic Engineers. It is one of the
            largest professional non-profit organisations in the world. IEEE
            defines network standards - for example, IEEE 802.3. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">IEEE 802.3:</span> The IEEE
            standard for Ethernet LAN that uses the CSMA/CD access protocol. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">IEEE 802.4:</span> The IEEE
            standard for LAN that uses token passing access protocol over a
            physical bus topology. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">IEEE 802.5:</span> The IEEE
            standard for LAN that uses token passing access protocol over a
            physical ring or star topology. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">IP Address:</span> A 32-bit
            long address assigned to hosts using TCP/IP. In dotted decimal
            format the address is written as four octets separated with periods
            that are made up of a network ID section, an optional subnet id
            section, and a host ID section. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="k">Kbps: </span>Stands for Kilo bits
            per second (one thousand bits per second). A unit of data transfer
            equal to 1000 bps.<a href="#top"> [Back to Top]</a><br><br>

            <span class="darkBlueText" id="l">LAN:</span> Stands for Local Area
            Network. A class of computer network covering a relatively small
            geographic area, e.g., a room, a building or a campus. A LAN is owned
            by a single organization and physically located within the
            organization's premises. Ethernet is the most popular LAN
            architecture. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Laser:
            </span>Stands for Light Amplification by Stimulated Emissions of
            Radiation. Represents a very pure and extremely narrow light beam.
            Such beans are used as the source of light in fibre-optic
            transmissions. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Local Loop:</span> A term that
            telephone companies use to refer to the wiring between the central
            office and a subscriber (e.g., an individual business or residence).
            A variety of technologies have been developed to provide high speed
            digital services over existing local loop wiring. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Logical Topology</span>: The way
            data is sent internally from one computer (or device) to another
            connected computer or device. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="m">MHz</span>: Megahertz - a
            measure of frequency equivalent to 1 million cycles per second. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="m">MAC</span>: Stands for Medium Access Control
            Protocol. <em>See channel access protocol</em>. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">MAC Address</span>: Also known as
            physical address. Each MAC address is a 48-bit binary address
            (normally written in hexadecimal notation). If the unique MAC address
            that exists on the NIC of the receiving computer is identical to the
            destination MAC address defined in a packet, the computer will
            attempt to receive the packet and process the data. All other
            computers on the network that have different physical addresses will
            not attempt to accept the packet for processing. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">MAN:</span> Stands for Metropolitan Area Network. A
            MAN is a backbone network that links multiple LANs in a large city or a metropolitan
            region.<a href="#top"> [Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Medium Access Control Protocol:</span> <i>See channel
                access protocol</i>. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Mesh Topology</span>: A network
            architecture in which a node has a point-to-point connection to other
            nodes(s). Since every node has a dedicated point-to-point link to
            every other device, mesh topology offers the highest throughput
            (i.e., faster speed) as well as better fault tolerance than any other
            topology. Mesh topologies are not very popular for LANs because they
            are very expensive and difficult to change. For example, to connect <b>n</b>
            devices on a network, you will need <b>n (n-1)/2</b> connections and
            <b>(n-1)</b> ports per device. Thus for a network of ten
            workstations, there will be 45 physical links (interfaces) between
            them. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="n">Network Interface Card:</span> <em>See NIC.</em>
            <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Network Traffic: </span>The
            network traffic denotes the number, the size, and the frequency of
            packets transmitted across a network in a given amount of time. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="l">NIC:</span> Stands for Network Interface Card. A hardware device
            (i.e.
            card) that plugs into a workstation, file server, printer, or other device to connect it to a network and to
            communicate with other network nodes. A NIC can be thought of as both a transmitter and a receiver (i.e. it is a
            transceiver). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Node: </span>Any device connected
            to a network such as a personal computer (PC), a mainframe computer,
            a router, a printer, or other network equipment.<a href="#top"> [Back to Top]</a><br><br>

            <span class="darkBlueText">Noise: </span>Unwanted electrical
            or electromagnetic energy that degrades the quality of signals and
            data. <a href="#top">[Back to Top]</a><br><br>
            <span class="darkBlueText">NOS: </span>Stands for Network Operating System. A
            large, complex program that can manage the common resources of a local area network.
            In addition, a NOS performs the standard operating system services. Examples of
            a NOS include NetWare, Linux, and MS Windows 2003.<a href="#top"> [Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="o">Optical Fibre:</span> A type of
            cable which consists of one or more glass or plastic fibre cores
            (fibres, strands) inside a protective cladding material, covered by
            an outer plastic PVC jacket. Signal transmission along the inside
            fibres is accomplished using light pulses. The optical fibre cable is
            characterised by extremely large data carrying capacity (i.e.,
            bandwidth). Optical fibre is used for undersea cables, and for
            country-wide telecommunications backbones. The Auckland University
            of Technology (AUT) uses optical fibre as a backbone cable to which
            all other AUT networks connect. <a href="#top">[Back to Top]</a><br><br>
            <span class="darkBlueText">OSI Model:</span> Stands for Open Systems Interconnection.
            A network architecture for developing network protocol standards. The OSI model
            formally defines and codifies the concept of layered network architecture. The
            OSI model consists of the following seven layers: (1) physical ; (2) data link;
            (3) network; (4) transport; (5) session; (6) presentation; and (7) application. <a href="#top">[Back
                to Top]</a><br>
            <br>

            <span class="darkBlueText" id="p">Packet:</span> A packet is a unit
            of data formatted for transmission over a network. A packet normally
            includes control information, a header that contains information
            about the packet's source and destination addresses, the data to be
            transmitted, and a footer that contains error-checking information.
            Sometimes the terms <b>packet</b> and <b>frame</b> are used
            interchangeably. A typical packet size for an Ethernet network is
            approximately 1500 octets (bytes). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Packet Switching:</span> A
            transmission method in which packets are transmitted over a
            networking medium that maintains several paths between the sender and
            the receiver. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Peer-to-Peer Network:</span> A
            class of networks in which a computer can communicate with any other
            networked computers on an equal or peer-like basis without going
            through an intermediary, such as a server or a dedicated host. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Physical Topology:</span> The way
            computers and other networking devices are connected (linked)
            physically. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Protocol:</span> A protocol is a
            collection of rules for formatting, ordering, and error-checking data
            sent across a network. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="r">Repeater:</span> A hardware
            device used to strengthen the signals transmitted on a network. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Ring Topology:</span> In this
            configuration, computers are connected in a closed ring, and the
            signal travels in one direction around the ring. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">RJ45:</span> Refers to a
            connector used with unshielded twisted-pair (UTP) cable.
            <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Router:</span> A computer or device that connects networks having the same or
            different
            access
            methods - such as Ethernet network to a token ring network. A router
            forwards packets to networks by using a decision-making process based
            on data stored in a routing table; the router discovers the most
            efficient route based on both the table data and on information
            supplied by the network manager. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="s">Signal: </span>The electric or electromagnetic
            encoding of data used for transmission. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Simplex:</span> A mode of data
            transmission in which a signal travels in one direction only. The
            flight arrival and departure time schedule displayed at the airport,
            and radio and TV broadcasting are typical example of simplex
            communications. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Star Topology:</span> In this
            configuration each computer has only one dedicated point-to-point
            link - to a central controller (a hub or a switch). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Switch:</span> Unlike a Hub, a
            switch allows multiple users to communicate simultaneously in order
            to achieve a higher throughput. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="t">Terminator:</span> A hardware
            device (usually a 50 ohms resistor) which is used to mark the end of
            cable (especially in a coaxial cable based LAN). The terminator
            absorbs signals on the line, thereby keeping them from bouncing back
            and being heard again by network stations. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">THz</span>: Terahertz. It is
            equivalent to 1 trillion hertz (i.e. 1000 GHz). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Token: </span>A special control
            frame (i.e. control information) that controls the access to the
            common transmission channel in a token ring or a token bus network.
            Possession of the token permits a node to transmit data. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Token Bus: </span>LAN
            architecture that uses a token passing access protocol over a bus
            topology. This LAN architecture is the basis for the IEEE 802.4 LAN
            specification. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Token Passing:</span> Token
            passing is an example of a LAN access protocol. Under this scheme,
            computers attached to a ring use a special, short control message
            called a token to gain access to the network. To send data, a
            computer must wait for the token to arrive, then transmit exactly one
            frame, and transmit the token to the next computer. When a token
            arrives, the computer temporarily removes the token from the ring and
            uses the ring to transmit data. When no computers have data to send,
            the token circulates around the ring at a high speed. Only one token
            exists on the ring at any time. Unlike the CSMA/CD Ethernet protocol,
            token passing schemes are both contention free and collision free. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Topology:</span> This term
            generally refers to the physical layout or configuration of a local
            area network or a wide area network. The way computers are connected
            in a network is called topology. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Twisted Pair:</span> Twisted pair
            (TP) consists of one or many pairs of wires twisted together. A TP
            can be either unshielded (UTP) or shielded (STP). A variety TP
            categories are available on the market today. The low category UTPs
