            <span class="darkBlueText" id="a">Amplifier: </span>A unidirectional, electronic device used to boost
            (amplify)
            signals, with the resulting performance measured in decibels. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Amplitude Modulation: </span>A method to add information to a sinned wave carrier
            signal. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Amplitude: </span>The terms used to describe the height of a sine wave. <a
                href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Analog Loopback: </span>A test that create the loop at the modem's telecommunication
            line
            interface to eliminate faults to the analog signal. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Analog: </span>Any data in the form of continuously variable physical qualities,
            which are
            "analogous" to the data source. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">ASK (Amplitude Shift Keying): </span>Assigning unique pattern binary digits to
            different
            amplitudes. ASK is a structure of modulation that represents digital data as variations in the amplitude of a
            carrier
            wave. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Asynchronous Transmission:</span> Sending data in serial format regardless of clock
            timing in
            a sender and receiver. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="b">Baseband:</span> A direct transmission method whereby the transmission
            medium
            carried only one signal at a time. Baseband usually applied for distance under 10 miles. <a href="#top">[Back
                to
                Top]</a><br><br>

            <span class="darkBlueText">Batch Processing:</span> Data processing technique whereby transactions are grouped
            together
            and processed as a batch. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Baud:</span> A unit used in the measurement of signaling bit or rate. Baud is usually
            defined
            as the number of signal level changes per second, regardless of information content of those signals. If each
            signal
            event represents only one bit condition, baud is the same as bits per second when each signal event represents
            other
            than one bit. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Bit Rate: </span>The speed at which binary digits (bits) would be transmitted over a
            communications path and usually expressed in "bits per second" (bps). <a href="#top">[Back to
                Top]</a><br><br>

            <span class="darkBlueText">Bit Stream: </span>The continuous stream of bits being transmitted over a
            communication path.
            <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="c">Cable: </span>The physical transmission medium of a group of metallic
            conductors or optical fibers that are bound together and wrapped in a protective cover. <a href="#top">[Back
                to
                Top]</a><br><br>

            <span class="darkBlueText">Carrier Wave:</span> The wave which a signal is placed upon. <a href="#top">[Back
                to
                Top]</a><br><br>

            <span class="darkBlueText">Channel Access Protocol:</span>
            Also known as media access control (MAC) protocol. A protocol (i.e.,
            set of rules) which is used to access the shared channel on the
            network. Examples of access protocols are Ethernet CSMA/CD, and Token
            passing. <a href="#top">[Back to Top]</a><br><br>


            <span class="darkBlueText">Circuit: </span>A communications path between two nodes. <a href="#top">[Back to
                Top]</a><br><br>

            <span class="darkBlueText">Coaxial Cable: </span>A transmission medium created as a two-conductor wire. It has
            wide
            bandwidth a small susceptibility to signal distortion since the signal is transmit in an enclosed environment.
            <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Communications Satellite: </span>A microwave repeater, orbiting in a typically
            geosynchronous
            orbit, designed to relay signals between communications stations. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">CRC:</span> Stands for Cyclic
            Redundancy Check. An error detection technique that typically adds
            either 16 or 32 "check" bits to potentially large data packets
            and provides almost 100 percent error detection. <a href="#top">[Back to Top]</a><br><br>


            <span class="darkBlueText"id="d">Data: </span>The representation of facts, instructions or concepts that are
            suitable
            for communication. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Data Frame: </span>Part of a transmission that contain data rather than control
            information.
            <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Decapsulation: </span>The process of stripping off one layer&rsquo;s headers and
            passing the
            rest of the packet up to the next higher layer on the protocol stack. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">DB25 Connector:</span> DB25 is a D-Subminiature connector which contains 25 pins for
            the male
            connector and 25 holes for the female connector. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">DB9 Connector:</span> Refers to a common connector type, one of the D-Subminiature or
            D-Sub
            types of connectors. DB9 has the smallest "footprint" of the D-Subminiature connectors, and houses 9 pins (for
            the male
            connector) or 9 holes (for the female connector). <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">DCE:</span> Data-circuit switching equipment provides the functions needed to create,
            maintain and terminate a connection. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Direct Connect Modems: </span>Modem that connects directly to a telephone line via
            modular
            connectors. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Distortion: </span>The unwanted changes in signal from its original form occurring
            during
            transmission. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">DTE: </span>Data terminal equipment such as computers, printers, PDAs that can be
            connected
            to a router or a modem. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText"id="e">Encapsulation: </span>The technique in which a data unit from one protocol is
            placed
            within the data field portion of the data unit of another protocol. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Ethernet</span>: A popular LAN
            technology (invented by Xerox Corporation and developed jointly by
            Xerox, Intel, and Digital Equipment Corporation) that uses a shared
            channel and the CSMA/CD access method. Basic Ethernet operates at 10
            Mbps, Fast Ethernet operates at 100 Mbps, and Gigabit Ethernet
            operates at 1000 Mbps (i.e., 1 Gbps). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="f">Fading: </span>Interference from radio transmission signals or
            microwave
            signal causing a received signal to deflect from the target. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Fiber Optics:</span> A transmission medium using glass fibers to carry light signal
            containing information. It has a capability of carrying a large amount of data and very secure. <a
                href="#top">[Back
                to Top]</a><br>
            <br>

            <span class="darkBlueText">FTP (File Transfer Protocols): </span>A network protocol used to transfer files from
            one host
            to another host over a TCP/IP network. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Frame: </span>The formatted
            packet that the underlying hardware accepts and delivers. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Frame Relay: </span>High spend packet switching protocol used in WANs. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Frequency Division Mmultiplexing: </span>A system in which the available frequency
            range is
            divided into narrow bands and each used its own dedicated channel. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">FSK (Frequency Shift Feying): </span>Digital information is transmitted through
            discrete
            frequency changes of a carrier wave. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Full-Duplex: </span>Two nodes transfer data simultaneously in both directions. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="h">Half-Duplex: </span>Two nodes transfer data simultaneously in both
            directions but not in the same time. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Hamming Code:</span> A method of error correction named created to detect and
            correct a
            single bit received in error. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Handshaking:</span> An automated process of negotiation, before normal communication
            establishes, two entities are dynamically assigned parameters of a communication channel. <a
                href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText">HDLC:</span> High-Level Data Link Control, transmission protocol used in Data Link
            Layer. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">High-Speed Modem:</span> Modems that operate at greater than 9600 bps. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Horizontal Redundancy Check: </span>See Longitudinal redundancy check. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="k">Kbps (Kilo Bits Per Second): </span>Thousands of bits per second
            (BPS). <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="l">Light Wave:</span> Fiber optic transmission utilizing very high
            frequency
            light signal. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">LLC:</span> Logical Link Control. It is the upper sub layer of the data link layer,
            responsible for data transmission between computers or devices on a network. <a href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText">Longitudinal Redundancy Check: </span>A method for error detection whereby the Block
            check
            character consists of bits based on either odd or even parity for all the characters in the transmission block.
            <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Low-Speed Modem: </span>Modems that operate at less than or equal 600 bps. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="m">MAC</span>: Stands for Medium Access Control
            Protocol. <em>See channel access protocol</em>. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">MAC Address</span>: Also known as
            physical address. Each MAC address is a 48-bit binary address
            (normally written in hexadecimal notation)
            . If the unique MAC address
            that exists on the NIC of the receiving computer is identical to the
            destination MAC address defined in a packet, the computer will
            attempt to receive the packet and process the data. All other
            computers on the network that have different physical addresses will
            not attempt to accept the packet for processing. <a href="#top">[Back to Top]</a><br><br>


            <span class="darkBlueText">Medium Access Control Protocol:</span> <i>See channel
                access protocol</i>.<a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Mbps:</span> Millions of bits per second (bps). <a href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText">Medium-Speed Modem: </span>Modems that operate between 1200 and 9600 bps. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">MHz: </span>One million cycles per second (Hz). <a href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText">Microwave radio broadcast: </span>a radio broadcast using a very high frequency
            radio
            signal. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Modem:</span> A device that allows 2 remote digital devices to communicate to each
            other
            through the analog environment. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Multiplexer: </span>A device that selects one of several analog or digital input
            signals and
            forwards the selected input into a single line. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="n">Network Interface Card:</span> <em>See NIC.</em>
            <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Network Topology: </span>A topology is a schematic description of the arrangement of
            a
            network, including its nodes and how it is connected. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">NIC:</span> Stands for Network Interface Card. A hardware device (i.e. card) that
            plugs into
            a workstation, file server, printer, or other device to connect it to a network and to communicate with other
            network
            nodes. A NIC can be thought of as both a transmitter and a receiver (i.e. it is a transceiver). <a
                href="#top">[Back
                to Top]</a><br><br>

            <span class="darkBlueText">Noise:</span> A communications line impairment induced by natural disturbances and
            therefore
            distort transmitted data. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="o">Optical fiber: </span>See Fiber Optic. <a href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText" id="p">Packet:</span> A packet is a unit
            of data formatted for transmission over a network. A packet normally
            includes control information, a header that contains information
            about the packet's source and destination addresses, the data to be
            transmitted, and a footer that contains error-checking information.
            Sometimes the terms <b>packet</b> and <b>frame</b> are used
            interchangeably. A typical packet size for an Ethernet network is
            approximately 1500 octets (bytes). <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Packet Header:</span> Information added to the beginning of the data sent from one
            OSI layer
            to another; typically contains addressing and sequencing information. <a href="#top">[Back to Top]</a><br />
            <br />

            <span class="darkBlueText">Parallel Transmission:</span> All bits that made up a character are transmitted at
            the same
            time. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Parity Bits: </span>A bit added to the end of a string of binary code that indicates
            whether
            the number of bits in the string with the value one is even or odd. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Parity Checking: </span>Refers to the use of parity bits to check that data has been
            transmitted accurately. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Parity Error: </span>A data error where an extra or missing bit is detected. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">PPP: </span>Point-to-Point Protocol. A protocol for data transfer across a serial
            line.<a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Protocol:</span> A protocol is a
            collection of rules for formatting, ordering, and error-checking data
            sent across a network. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">PSK (Phase Shift Keying):</span> A digital modulation scheme that transmits data by
            changing, or modulating, the phase of a reference carrier wave. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="r">Radio Broadcast: </span>A wireless transmission method whereby
            signal is
            carried by radio waves and is intended to reach a wide range of audiences. <a href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText">Router: </span>A router is specialized computer connected to more than one network
            running
            software that allows the router to move data from one network to another. <a href="#top">[Back to
                Top]</a><br><br>

            <span class="darkBlueText" id="s">Serial Transmission: </span>Data is transmitted one bit at a time
            across a
            channel (a single wire). <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">SMTP:</span> Simple Mail Transfer Protocol, provides electronic mail facility across
            the
            network. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">SMNP: </span>Simple Management Network Protocol: part of the TCP/IP Suite that
            monitors
            remote hosts. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Statistical Multiplexing:</span> A communication channel is divided into an
            arbitrary number
            of variable bit-rate digital channels or data streams. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Store-And-Forward:</span> A method of data communications that allows transactional
            information to be collected and stored until is forward in a predetermined time. <a href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText">Switch:</span> Unlike a Hub, a
            switch allows multiple users to communicate simultaneously in order
            to achieve a higher throughput. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Synchronization:</span> Blocks of data are transmitted with the sender and the
            receiver
            being in sync by a timing device. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="t">Telnet: </span>Telecommunication Network Protocol provide a
            bidirectional
            interactive text-oriented communication facility which support remote host connection. <a href="#top">[Back
                to
                Top]</a><br>
            <br>

            <span class="darkBlueText">Time Division Multiplexing:</span> A method of transmitting and receiving
            independent
            signals over a common signal path by means of synchronized switches at each end of the transmission line. <a
                href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Time to Live:</span> A mechanism to limit the time a packet can travel in a network.
            <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText">Topology:</span> This term
            generally refers to the physical layout or configuration of a local
            area network or a wide area network. The way computers are connected
            in a network is called topology. <a href="#top">[Back to Top]</a><br>
            <br>


            <span class="darkBlueText">Transmission Error:</span> Data is corrupted during transmission. <a
                href="#top">[Back
                to Top]</a><br>
            <br>

            <span class="darkBlueText">Transmission Speed: </span>A measure of how quickly data is being conveyed, usually
            expressed in bps. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Transmission: </span>The sending of information over a medium. <a
                href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText">Transmit:</span> To send out a signal over a medium such as optical fiber, copper
            wire, or
            via radio signal. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Twisted Pair:</span> A type of wiring in which two conductors of a single circuit
            are
            twisted together in order to eliminate electromagnetic interference from external sources. <a
                href="#top">[Back to
                Top]</a><br>
            <br>

            <span class="darkBlueText" id="u">UTP:</span> Unshielded Twisted Pair, cable that consists of two
            unshielded
            wires twisted around each other. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText" id="v">Vertical Redundancy Check:</span> A parity check is applied to a
            group which
            is all bits in one character. <a href="#top">[Back to Top]</a><br>
            <br>

            <span class="darkBlueText">Virtual Circuit:</span> A data circuit that exists only for the duration of a call
            but
            sharing data channels with other calls. <a href="#top">[Back to Top]</a><br><br>

            <span class="darkBlueText" id="w">WEP:</span> Stands for Wired Equivalent Privacy. Used as a security
            protocol
            in <em>IEEE 802.11b </em> (<em>Wi-Fi</em>) networks. WEP employs 64-bit or 128-bit encryption. <a
                href="#top">[Back
                to Top]</a><br /><br />

            <span class="darkBlueText">Wi-Fi: </span>Stands for Wireless Fidelity and was originally a name used for
            <em>IEEE
                802.11b</em>. Refers to any <em>IEEE 802.11 </em> wireless networking standard. <a href="#top">[Back to
                Top]</a><br />
            <br />


            <span class="darkBlueText">Wireless: </span>A network connection which deploys the transmission of
            electromagnetic
            frequency through the atmosphere to carry data transmissions. <a href="#top">[Back to Top]</a><br />
            <br />


            <span class="darkBlueText">Word: </span>The stream of either bits or characters capable of being stored and
            processed
            as a unit. <a href="#top">[Back to Top]</a><br>
            <br>

