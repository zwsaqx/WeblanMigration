<li>the forms is then automatically forwarded by a commercial form forwarding company (called Response-o-course)  to John Glenn -  the Course Advisor.</li> 
<li>John receives the form as an email.</li> 
<li>John evaluates the course selections.</li>
<li>John writes back to the student using email.</li>
</ul>
