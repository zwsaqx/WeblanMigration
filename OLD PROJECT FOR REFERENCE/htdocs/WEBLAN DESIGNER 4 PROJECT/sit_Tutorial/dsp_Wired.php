<div align="center" class="darkBlueTextJumbo">Wired LAN Design Tutorial</div>
<div align="center" class="redText">Time: 60 minutes</div>
  
<p><br>
<span class="darkBlueText">Learning Outcomes</span> <br />
<i>By the end of this tutorial, you will be able to: </i><br />

<ol>
  <li>Define the following key terms related to LAN design: Bus, star, and 
    ring physical topologies, logical topology, channel, channel access protocol, 
    CSMA/CD, token passing, workstation, file server, hubs, switches, router, 
    UTP Cat 5e, coaxial cable, and optical fibre.</li>
  <li>Develop various LAN models using the modelling feature of WebLan-Designer 3.</li>
  <li>Solve LAN design exercises on paper and verify the results using WebLan-Designer 3.</li>
  <li>Assess your knowledge of networking through the interactive quizzes.</li>
  <li>Solve scenario based LAN design exercises.</li>
</ol> 
