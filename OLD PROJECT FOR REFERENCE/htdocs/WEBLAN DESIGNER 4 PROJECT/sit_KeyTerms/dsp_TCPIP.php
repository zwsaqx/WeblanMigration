<span class="darkBlueText" id="a">Address Resolution Protocol: </span>A protocol that resolves logical IP addresses to physical addresses. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Adjacency: </span>An adjacency is created when two routers can exchange routing information with one another. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Advertisement: </span>Advertisement is a method that a router uses to announce itself to other devices on the network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">AP: </span>Stands for Access Point. Typically, infrastructure-based wireless networks provide access to the wired backbone network via an AP. The AP may act as a repeater, bridge, router, or even as a gateway to regenerate, forward, filter, or translate messages. All communications between mobile devices have to take place via the AP. <a href="#top">[Back to Top]</a><br>
<br>


<span class="darkBlueText">Application Layer: </span>The layer of the TCP/IP stack that supports network applications and provides an interface to the users. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Autonomous System: </span>A single network or group of network that has the same policy and is controlled by 1 network administrator. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="b">Bridge: </span>A bridge is a device that forwards traffic between network segments. These segments share a common network layer address space. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Broadcast domain: </span>Is a logical division of a computer network, in which all nodes can reach each other by broadcasting. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Broadcasting:</span> Transmitting of packet that will be received by all devices on the network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="c">CIDR:</span> Classless Internet Domain Routing. A technique that allow a block of IP addresses to be represented as a single entity. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Classfull Addressing:</span> A concept of subnetting in which all IP addresses of a network use only 1 subnet mask. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Classless Addressing:</span> A concept of subnetting in which a network can have several subnet masks, with no consideration of Class A, B, and C rules for network classification. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Collision Domain:</span> A section of a network where data packets can collide with one another when being sent on a shared medium. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Convergence:</span> The state of a set of routers in which they all have the same routing information of the network they are operated in. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="d">Datagram:  </span>The data package passed from the Internet layer to the Network Access layer, or a data package passed from UDP at the Transport layer to the Internet layer. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="d">Data Link Layer:</span> Layer 2 in the OSI reference 
model. Main responsibilities include access to the networking medium (physical 
layer) and error-free delivery of data frames. <a href="#top">[Back to Top]</a><br>
<br>

<span class="darkBlueText">Default Route: </span>On a router, the route that is considered to match all packets that are not otherwise matched by any other routes. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Distance Vector Routing Rrotocols: </span>DVRP requires routing hardware to report the distances of various nodes within a network in order to determine the best and most efficient routes for data packets. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Domain Name System: </span>is a hierarchical distributed naming system for computers, services, or any resource connected to the Internet or a private network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">DUAL: </span>The Diffusing Update ALgorithm, is the algorithm used by Cisco's EIGRP routing protocol to ensure that a given route is recalculated globally whenever it might cause a routing loop. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Dynamic Routing Protocols: </span>Routing that adjust automatically to network topology changes. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="e">EIGRP</span>: A network protocol that lets routers exchange information more efficiently than with earlier network protocols. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Exterior Gateway Protocol: </span>A protocol for exchanging routing information between 2 or more autonomous systems. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="f">Frame: </span>The data package created at the Network Access layer. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="g">Gateway: </span>A gateway is a program or piece of hardware that allows data to be transferred between networks. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="h">Header:</span> A group of information attached to the data at each layer of the protocol stack. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Hold-Down Timer: </span>A mechanism used to prevent bad routes from being restored and propagated by mistake. When a route is placed in a hold-down state, routers will neither advertise the route nor accept advertisements about it for a specific interval called the hold-down period. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Hop:</span> A hop is one part of the path between a source and a destination which can contains several hops. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Hop Count:</span> Distance between two hosts. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Hop Limit:</span> see <i>Time to Live</i>. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Hybrid Routing Protocol:</span> Hybrid routing protocols have both the features of distance vector routing protocols and linked state routing protocols. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">IEEE 802.11:</span> The original standard for wireless LAN with a maximum bandwidth of 1- and 2-Mbps operating in the 2.4 GHz band, adopted in 1997. <a href="#top">[Back to Top]</a><br/><br/>

<span class="darkBlueText" id="i">Interior Gateway Protocol: </span>A protocol for exchanging routing information within an autonomous system. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Internet Layer:</span> The layer of the TCP/IP stack that provides logical addressing and routing. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">IP Address:</span> A set of 4 numbers that are separated by a decimal point that represent the numeric address of of a computer attached to a TCP/IP network, such as the Internet. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">IPv4:</span> is the fourth revision of the IP and a widely used protocol in data communication over different kinds of networks. IPv4 is a connectionless protocol used in packet-switched layer networks. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="l">Latency: </span>The amount of time it takes a packet to travel from source to destination. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Link-State Routing Protocols:</span> Routing protocols whose algorithms calculate the best paths to networks. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Logical Topology:</span> The way that the data passes through the network from one device to the next regardless of physical aspect such as geographic. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Loopback Address:</span> A special IP number (127.0.0.1) that is designated for the software loopback interface of a machine. The loopback interface is not physically exist. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="m">MAC Address: </span>Short for Media Access Control address, a hardware address that uniquely represent each node of a network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Message:</span> In TCP/IP networking, a message is the data package passed from the Application layer to the Transport layer. The term is also used generically to describe a message from one entity to another on the network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Metric:</span> Is a property of a route consisting of any value used by a routing protocol to determine whether one particular route should be chosen. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Multicast: </span>The delivery of a message or information to a group of destination devices simultaneously in a single transmission. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="n">Network Access Layer: </span>The layer of the TCP/IP stack that provides an interface with the physical network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Network Topology: </span>A topology is a schematic description of the arrangement of a network, including its nodes and how it is connected. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Next Hop: </span>Routing term used for the next gateway to which packets should be sent to in order to reach its final destination.<a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Node: </span>Can be data circuit-terminating equipment (DCE) such as a modem, hub, bridge or switch; or data terminal equipment (DTE) such as a printer or a host computer, for example a router, a workstation or a server. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="o">OSPF:</span> Open Shortest Path First: a link-state routing protocol that determines the best path for a packet through an internetwork.<a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="p">Packet:</span> In networking, a specially
 organized and formatted collection of digital data. Different protocols use
 different names for their packets - for example ATM packets are called &quot;cells&quot;. <a href="#top">[Back to Top]</a><br/> <br/>
 
  <span class="darkBlueText">Packet Header:</span> Information added to the beginning of the data sent from one OSI layer to another; typically contains addressing and sequencing information. <a href="#top">[Back to Top]</a><br/>
 <br/>

<span class="darkBlueText">Payload:</span> The data portion of a <em>packet</em>, which carries the <em></em>meaningful information communicated between sender and receiver. <a href="#top">[Back to Top]</a><br/> <br/>


<span class="darkBlueText" id="p">Physical Topology</span>: The way that networking devices and nodes are connected to the network through the actual cables that transmit data.<a href="#top"> [Back to Top]</a><br><br>

<span class="darkBlueText">Private Address</span>: IP addresses in several Class A, B, and C networks that are set aside for use inside private organizations. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Private Address</span>: An IP address that is part of a registered network number, so that only the organization to which the address is registered is allowed to use. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="r">RIP:</span> Routing Information Protocol, RIP establishes communication between routers to share routing information. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Route Aggregation: </span>Enables you to use a larger network to represent smaller networks. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Route Poisoning: </span>When a router detects that one of its connected routes has failed, the router will assign an infinite metric to it. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Route Summarization: </span>see <i>Route Aggregation</i>. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Router: </span>A router is specialized computer connected to more than one network running software that allows the router to move data from one network to another. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Routing Algorithm: </span>Is a way for one network device to calculate which other device or network link to use to reach a particular other network device or group of network devices. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Routing Loop: </span>A serious network problem which happens when a data packet is continually routed through the same routers over and over. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Routing Protocol: </span>A routing protocol specifies the rules and policy on how networking devices communicate with each other. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Routing Table: </span>Data table stored in a router or a networked computer that lists the routes to particular destinations alongside with metrics in some cases. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Routing: </span>A device or setup that calculate and find the optimal route for packets to traverse between two or between more than two nodes. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="s">Segment:</span> The data package passed from TCP at the Transport layer to the Internet layer. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Slash Notation: </span>Slash notation is a method to simplify a netmask. In slash notation, a single number indicates how many bits of the Internet Protocol (IP) address identify the network the host is on. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Split Horizon: </span>A routing configuration that stops a route from being advertised back in the direction from which it came. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Static Routing Protocols: </span>Is the process in which the system network administrator would manually configure network routers in order for packets to be transferred smoothly. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Sub-Interface: </span>A logical division of a physical interface that borrows the bandwidth it needs from the physical interface from which it stems. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Subnet Mask: </span>A 32-bit number that masks an IP address, and divides the IP address into network address and host address. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Subnet: </span>A segment of a network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Supernet: </span>see <i>Route Aggregation</i>. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="t">TCP:</span> One of the main protocols in TCP/IP networks, TCP enables two hosts to create a connection and start exchanging data. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Time to Live:</span> A mechanism to limit the time a packet can travel in a network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Trace Route:</span> A computer network diagnostic tool for displaying the route (path) and measuring transit delays of packets across an Internet Protocol (IP) network. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText">Transport Layer:</span> The layer of the TCP/IP stack that provides error control and acknowledgement and serves as an interface for network applications. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="u">Unicast:</span> The sending of messages to a single network destination identified by a unique address. <a href="#top">[Back to Top]</a><br><br>

<span class="darkBlueText" id="v">VLSM:</span> The capability use different subnet masks in a network. <a href="#top">[Back to Top]</a><br><br>

